import mongoose from "mongoose";

// the schema has to have a name, password, phone, email and address
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  phone: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  address: {
    type: String,
    required: true
  }
})

export const User = mongoose.model("User", userSchema);